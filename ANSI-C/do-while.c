//---------------------------------------------------------------
// do-while statement.
//---------------------------------------------------------------
#include <stdio.h>

int main() {
	int x = 3;
	int *xp;
	int y = 0;
	
	xp = &x;
	
	do {
		printf("%d, ", *xp * y);
		++y;
	} while(y<=10);
	printf("\n");
}
